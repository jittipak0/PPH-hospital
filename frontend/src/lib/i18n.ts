type I18nContextValue = {
  language: Language
  switchLanguage: (language: Language) => void
  t: (key: string) => string
}

type I18nProviderProps = {
  children: ReactNode
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined)

export function I18nProvider({ children }: I18nProviderProps): ReactElement {
  const [language, setLanguage] = useState<Language>(() => {
    if (typeof window === 'undefined') {
      return 'th'
    }
    const stored = window.localStorage.getItem('preferredLanguage')
    return stored === 'th' || stored === 'en' ? stored : 'th'
  })

  const switchLanguage = useCallback((next: Language) => {
    setLanguage(next)
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('preferredLanguage', next)
    }
  }, [])

  const value = useMemo<I18nContextValue>(() => ({
    language,
    switchLanguage,
    t: (key) => dictionary[language][key] ?? key
  }), [language, switchLanguage])

  return createElement(I18nContext.Provider, { value }, children)
}

export const useI18n = (): I18nContextValue => {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within I18nProvider')
  }
  return context
}
